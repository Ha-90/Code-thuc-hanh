
// Bài 2: Nhập 3 số bất kỳ, tìm số lớn nhất.
let a = prompt("Nhập số bất kỳ thứ 1");
let b = prompt("Nhập số bất kỳ thứ 2");
let c = prompt("Nhập số bất kỳ thứ 3");
if (a > b && a > c) {
    document.write(a + " là số lớn nhất");
} 
else if (b > c ) {
     document.write(b + " là số lớn nhất");
} 
else { 
    document.write(c + " là số lớn nhất");
} 



